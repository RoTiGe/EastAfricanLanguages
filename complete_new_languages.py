def load_json(filepath):
    """Load JSON file"""
    with open(filepath, 'r', encoding='utf-8') as f:
        return json.load(f)

def save_json(filepath, data):
    """Save JSON file with proper formatting"""
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def complete_language(lang_code):
    """Complete all missing translations for a language"""
    print(f"\n{'='*60}")
    print(f"Processing {lang_code.upper()}...")
    print(f"{'='*60}")
    
    # Load reference and target language files
    reference = load_json(f'translations/{REFERENCE_LANG}.json')
    target_file = f'translations/{lang_code}.json'
    target = load_json(target_file)
    
    field_name = LANG_FIELDS[lang_code]
    
    # Add categoryNames if missing
    if 'categoryNames' not in target:
        target['categoryNames'] = CATEGORY_NAMES[lang_code]
        print(f"âœ“ Added categoryNames")
    
    # Track statistics
    categories_added = 0
    phrases_added = 0
    
    # Process each category from reference
    for category_key, phrases in reference['categories'].items():
        if category_key not in target['categories']:
            target['categories'][category_key] = []
            categories_added += 1
            print(f"  + Added category: {category_key}")
        
        # Create lookup for existing phrases in this category
        existing_phrases = {p.get('english', '').lower(): p for p in target['categories'][category_key]}
        
        # Process each phrase
        for ref_phrase in phrases:
            english_key = ref_phrase.get('english', '').lower()
            
            if english_key not in existing_phrases:
                # Create new phrase with translation
                new_phrase = ref_phrase.copy()
                
                # Try to find translation
                if english_key in TRANSLATIONS[lang_code]:
                    new_phrase[field_name] = TRANSLATIONS[lang_code][english_key]
                else:
                    # Use English as fallback
                    new_phrase[field_name] = ref_phrase['english']
                
                target['categories'][category_key].append(new_phrase)
                phrases_added += 1
            else:
                # Phrase exists, ensure it has the language field
                existing = existing_phrases[english_key]
                if field_name not in existing:
                    if english_key in TRANSLATIONS[lang_code]:
                        existing[field_name] = TRANSLATIONS[lang_code][english_key]
                    else:
                        existing[field_name] = ref_phrase['english']
                    phrases_added += 1
    
    # Save updated file
    save_json(target_file, target)
    
    # Calculate final statistics
    total_categories = len(reference['categories'])
    total_phrases = sum(len(phrases) for phrases in reference['categories'].values())
    final_categories = len(target['categories'])
    final_phrases = sum(len(phrases) for phrases in target['categories'].values())
    
    print(f"\nğŸ“Š Statistics:")
    print(f"  Categories: {final_categories}/{total_categories} ({final_categories/total_categories*100:.1f}%)")
    print(f"  Phrases: {final_phrases}/{total_phrases} ({final_phrases/total_phrases*100:.1f}%)")
    print(f"  Added: {categories_added} categories, {phrases_added} phrases")
    
    return {
        'language': lang_code,
        'categories_added': categories_added,
        'phrases_added': phrases_added,
        'total_categories': final_categories,
        'total_phrases': final_phrases
    }

