def create_expanded_translation(lang_code, ui_data, lang_name_native):
    """Create a fully expanded translation file for a language"""
    
    result = {
        "language": lang_code,
        "nativeLanguageField": lang_code,
        "ui": ui_data,
        "categoryNames": category_translations[lang_code],
        "categories": {}
    }
    
    # Get word translations for this language
    translations = word_translations[lang_code]
    
    # Process each category from the template
    for category_key, category_items in template['categories'].items():
        result['categories'][category_key] = []
        
        for item in category_items:
            english_word = item['english']
            
            # Create new item with all language fields
            new_item = item.copy()
            
            # Add translation for this language if available
            if english_word in translations:
                new_item[lang_code] = translations[english_word]
            else:
                # Use English as fallback
                new_item[lang_code] = english_word
            
            result['categories'][category_key].append(new_item)
    
    return result

